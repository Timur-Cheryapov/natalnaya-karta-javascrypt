const selectRandomPhrase = phrases => phrases[Math.floor(Math.random() * phrases.length)]

const makeUpSentence = data => {
    let sentence = ''
    const parts = Object.keys(data)
    
    for (let part of parts) {
        sentence += selectRandomPhrase(data[part])
        if (part === parts[parts.length - 2]) {
            sentence += ': '
        } else {
            sentence += ' '
        }
    }

    return sentence
}

